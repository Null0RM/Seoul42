int	ft_is_prime(int nb)
{
	long long	i;
	long long	nbr;

	nbr = nb;
	i = 2;
	if (nbr < 2)
		return (0);
	while (i * i <= nbr)
	{
		if (nbr % i == 0)
			return (0);
		i++;
	}
	return (1);
}
