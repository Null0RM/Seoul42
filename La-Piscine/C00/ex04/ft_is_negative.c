#include <unistd.h>

void	ft_is_negative(int n);

void	ft_is_negative(int n)
{
	int	i;

	if (n < 0)
	{
		i = 'N';
		write(1, &i, 1);
	}
	else
	{
		i = 'P';
		write(1, &i, 1);
	}
}
